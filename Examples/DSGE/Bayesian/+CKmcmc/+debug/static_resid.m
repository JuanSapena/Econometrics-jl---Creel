function [lhs, rhs] = static_resid(y, x, params)
T = NaN(4, 1);
lhs = NaN(11, 1);
rhs = NaN(11, 1);
T(1) = params(1)*exp(y(6))*y(3)^(params(1)-1);
T(2) = y(4)^(1-params(1));
T(3) = y(3)^params(1);
T(4) = y(4)^(-params(1));
lhs(1) = y(8);
rhs(1) = y(2)^(-params(4));
lhs(2) = y(9);
rhs(2) = params(10)*exp(y(7));
lhs(3) = y(10);
rhs(3) = T(1)*T(2);
lhs(4) = y(11);
rhs(4) = exp(y(6))*(1-params(1))*T(3)*T(4);
lhs(5) = y(8);
rhs(5) = y(8)*params(2)*(1+y(10)-params(3));
lhs(6) = y(9)/y(8);
rhs(6) = y(11);
lhs(7) = y(6);
rhs(7) = y(6)*params(6)+params(7)*x(1);
lhs(8) = y(7);
rhs(8) = y(7)*params(8)+params(9)*x(2);
lhs(9) = y(1);
rhs(9) = T(2)*exp(y(6))*T(3);
lhs(10) = y(5);
rhs(10) = y(1)-y(2);
lhs(11) = y(3);
rhs(11) = y(5)+y(3)*(1-params(3));
end
